/******************************************************************************

	ticker.c

	時間計測用カウンタ関数 (64bit)

******************************************************************************/

#include "psp.h"

/*--------------------------------------------------------
	現在のカウンタを取得
--------------------------------------------------------*/

TICKER ticker(void)
{
	UINT64 current_ticks;

	sceRtcGetCurrentTick(&current_ticks);
	return current_ticks;
}
